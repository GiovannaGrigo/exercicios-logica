// Contador de 0 a 10 usando o laço de repetição For

for (let i = 0; i <= 10; i++) {
    console.log(i);
}


// Contador de 0 a 10 usando o laço de repetição For, dessa vez contando de 2 em 2
for (let i = 0; i <= 10; i += 2) {
    console.log(i);
}


// Contador de 10 a 0 usando o laço de repetição For
for (let i = 10; i >= 0; i--) {
    console.log(i);
}